# Variable Names (RULES)
# A variable name must start with a letter or the underscore character
# A variable name cannot start with a number
# A variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ )
# Variable names are case-sensitive (age, Age and AGE are three different variables)
# A variable name cannot be any of the Python keywords.#

myvar = "John"
my_var = "John"
_my_var = "John"
myVar = "John"
MYVAR = "John"
myvar2 = "John"

# Multi Words Variable Names
# Camel Case
# In Camel Case first Letter of First word is small while the First letter of other words is capital. 
myVariableName = "John"

# Pascal Case
# In Pascal Case first Letter of All word is capital. 
MyVariableName = "John"

# Snake Case
# In Pascal Case first Letter of All word is small but spereated by hypen(_). 
my_variable_name = "John"
