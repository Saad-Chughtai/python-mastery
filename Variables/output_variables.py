# Output Variables
# For output we use Print() function
x = "Coding is awesome"
print(x)
"""
Output:
Coding is awesome
"""

# U can output multiple variables, separated by a comma:
x = "Coding"
y = "is"
z = "awesome"
print(x, y, z)
"""
Output:
Coding is awesome
"""

# You can also use the + operator to output multiple variables:
x = "Coding "
y = "is "
z = "awesome"
print(x + y + z)
"""
Output:
Coding is awesome
"""
# You can add same datatype only 
x = 5
y = 10
print(x + y)
"""
Output:
15
"""
# Invalid / Error
x = 5
y = "Saad"
print(x + y)

# Best way to output multiple variables in the print() function is to separate them with commas,
# which even support different data types:
x = 5
y = "Saad"
print(x, y)
"""
Output:
5 Saad
"""