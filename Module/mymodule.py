# Python Modules

"""
What is a Module?
A file containing a set of functions you want to include in your application, Same like a code library.
"""

def greeting(name):
  print("Hello, " + name)

"""
Variables in Module:
    The module can contain functions, as already described, but also variables of all types (arrays, dictionaries, objects etc):
"""
